<template>
  <div>
    <!-- Job Details -->
    <div v-if="loading" class="text-center fs-4 fw-bold text-primary">Loading...</div>
    <div v-else-if="error" class="text-center fs-4 fw-bold text-danger">Error loading job</div>
    <div v-else class="job-details-container">
      <h1 class="heading">{{ job.title }}</h1>
      <div class="job-info">
        <p class="location">
          <i class="fas fa-map-marker-alt"></i> {{ job.city }}, {{ job.country }}
        </p>
        <p class="experience">
          <i class="fas fa-briefcase"></i> {{ job.min_exp_years }} - {{ job.max_exp_years }} years
          experience
        </p>
        <p class="job-type"><i class="fas fa-clock"></i> {{ job.type }} ({{ job.remote_type }})</p>
      </div>
      <div class="description" v-html="job.description"></div>
      <div class="requirements">
        <h2>Job Requirements</h2>
        <ul>
          <li v-for="requirement in job.requirements.split('\n')" :key="requirement">
            {{ requirement.trim() }}
          </li>
        </ul>
      </div>
      <div class="actions mt-5">
        <!-- Apply Button -->
        <a href="#" class="btn btn-primary" @click.prevent="showApplyModal">Apply Now</a>
        <a href="#" class="btn btn-secondary">Copy Job link</a>
      </div>
    </div>

    <!-- Apply Modal -->
    <div v-if="authStore.isLogged" class="modal fade " tabindex="-1" role="dialog" :class="{ 'show': showModal, 'd-block': showModal }">
      <div class="modal-dialog shadow-lg" role="document">
        <div class="modal-content">
          <div class="modal-header">
            <h5 class="modal-title me-5">Apply for <span class="text-primary">{{ job.title }}</span> job</h5>
            <button type="button" class="btn-close" data-bs-dismiss="modal" aria-label="Close" @click="hideApplyModal">
            </button>
          </div>
          <div class="modal-body">
            <div class="form-group">
              <label for="cv">Upload CV (Optional)</label>
              <input type="file" class="form-control-file" id="cv" @change="handleFileChange">
            </div>
          </div>
          <div class="modal-footer">
            <button type="button" class="btn btn-secondary" @click="hideApplyModal">Cancel</button>
            <button type="button" class="btn btn-primary" @click="applyForJob">Apply</button>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import api from '@/utilities/axios';
import { toast } from 'vue3-toastify'
import { useUserStore } from '@/stores/user'


export default {
  data() {
    return {
      job: {
        title: '',
        city: '',
        country: '',
        min_exp_years: 0,
        max_exp_years: 0,
        type: '',
        remote_type: '',
        description: '',
        requirements: ''
      },
      loading: false,
      error: false,
      showModal: false,
      resume_path: ''
    };
  },
  mounted() {
    this.fetchJob();
  },
  setup() {
    const authStore = useUserStore();
    return {
      authStore,
    };
  },
  methods: {
    async fetchJob() {
      this.loading = true;
      try {
        const response = await api.get(`/jobs/${this.$route.params.id}`);
        this.job = response.data;
        this.loading = false;
      } catch (error) {
        console.error('Error fetching job:', error);
        this.error = true;
        this.loading = false;
      }
    },
    showApplyModal() {
      this.showModal = true;
    },
    hideApplyModal() {
      this.showModal = false;
      // Reset resume_path when modal is hidden
      this.resume_path = null;
    },
    handleFileChange(event) {
      this.resume_path = event.target.files[0];
    },
    async applyForJob() {
  try {
    const response = await api.post(`/job-posts/${this.$route.params.id}/applications`, this.resume_path);
    console.log('Application posted successfully:', response.data);
    toast.success('Application posted successfully')
    this.hideApplyModal();
  } catch (error) {
    console.error('Error posting application:', error);
    toast.error('You have already applied for this job post:' + error);
  }
},

  },
};
</script>

